def coor_2_cen(x1, x2, y1, y2):
    xx = round((x1 + x2) / 2)
    yy = round((y1 + y2) / 2)
    return xx * 10000 + yy

def cen_2_cen_pair(cc):
    cx = round(cc / 10000)
    cy = cc % 1000
    return [cx, cy]

def cen_pair_2_cen(cx, cy):
    cc = cx * 10000 + cy
    return cc

def dis(ca, cb):
    return (ca[0] - cb[0]) * (ca[0] - cb[0]) + (ca[1] - cb[1]) * (ca[1] - cb[1])